    public void tampilkanData(){
        String [] kolom = {"Kode", "Paket", "Harga"};
        ArrayList <Makan> list = new Makan().getAll();
        Object rowData [] = new Object [3];
        
        tblMakan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Makan ma : list) {
            rowData[0] = ma.getKode_makan();
            rowData[1] = ma.getPaket();
            rowData[2] = ma.getHarga();
            
            ((DefaultTableModel)tblMakan.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String [] kolom = {"Kode", "Paket", "Harga"};
        ArrayList <Makan> list = new Makan().search(keyword);
        Object rowData [] = new Object [3];
        
        tblMakan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Makan ma : list) {
            rowData[0] = ma.getKode_makan();
            rowData[1] = ma.getPaket();
            rowData[2] = ma.getHarga();
            
            ((DefaultTableModel)tblMakan.getModel()).addRow(rowData);
        }
    }

