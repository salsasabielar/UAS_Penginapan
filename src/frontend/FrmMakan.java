    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblMakan.getModel();
        int row = tblMakan.getSelectedRow();

        Makan ka = new Makan().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        ka.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Makan ma = new Makan();
        ma.setKode_makan(Integer.parseInt(txtKodeMakan.getText()));
        ma.setPaket(txtPaket.getText());
        ma.setHarga(Integer.parseInt(txtHarga.getText()));
        ma.save();
        txtKodeMakan.setText(Integer.toString(ma.getKode_makan()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

